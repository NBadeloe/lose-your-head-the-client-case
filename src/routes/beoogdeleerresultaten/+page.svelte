		<h4>1.4 Ontwikkelingen</h4>
			<p>
				Naast het nieuwe competentieprofiel hebben maatschappelijke thema’s zoals diversiteit en inclusiviteit een nog explicietere plek in het curriculum en de organisatie gekregen. In het curriculum is een nieuwe minor “exclusive design” toegevoegd. Ook krijgt toegankelijkheid en duurzaamheid van digitale producten aandacht in vakken en projecten. In de visie is een sectie over Diversiteit/Inclusiviteit, Digitalisering en Duurzaamheid opgenomen waarbij ook gezorgd is voor concrete handvatten voor docenten en organisatie. 

				Een laatste substantiële verandering die na de laatste visitatie in gang is gezet, is de rol van techniek binnen de opleiding. CMD Amsterdam is een van de meest technische CMD opleiding van Nederland. Voorheen bestond techniek vooral uit frontend development, dit vatten we nu breder op en we kijken voorbij alleen grafische, veelal schermgebaseerde interfaces. Zo bieden we in het derde jaar een semester Designing for Emerging Technologies aan, een minor Conversational Design, een minor Immersive Environments en in de propedeuse een nieuw vak Emerging Technologies waar Artificial Intelligence, fysieke digitale interfaces en Augmented Reality de revue passeren. 
			</p>
