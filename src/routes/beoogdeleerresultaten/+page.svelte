		<h4>1.5 Ambities en dilemma’s</h4>
			<p>
				Ambities verbeterpunten met bijbehorende veranderingen die we in gang hebben gezet of op korte termijn gaan ontwikkelen en doorvoeren.
				•	Een ambitie bij CMD Amsterdam is al enkele jaren om de zelfstandigheid van studenten te vergroten. Studenten hebben vaak de neiging om aan te sluiten op de wensen van de docent en dit leidt vaak tot veilige keuzes. Wij zijn op zoek naar manieren om dit te veranderen en ze te stimuleren buiten de lijntjes te kleuren en minder “af te vinken”. Op dit moment zijn we in een aantal onderwijsmodules aan het experimenteren met meer eigen invulling hoe te werken aan competenties en programmatisch toetsen (zie hiervoor standaard 3).
				•	De Studieloopbaan Begeleiding (SLB) in het propedeuse jaar heeft in 2021/2022 een grondige update gehad. Dit update is erop gericht om meer aandacht te besteden aan het ontwikkelen van zelfregulatie bij studenten naast het bespreken van praktische schoolzaken. Dit doen we door studenten krachtgericht te coachen en ze aan te moedigen hun ontwikkeling te verbeelden met aangeboden ‘visual thinking’ technieken. Dit SLB-programma zal ook doorgetrokken worden naar jaar 2 en 3 met name om de student inzicht te geven in zijn/haar unieke rol als ontwerper.

				Dilemma’s vragen waarover we graag met het panel in gesprek zouden willen.
				•	Een goede inventarisatie van de Body of Knowledge Skills and Attitude (BoKSA) ontbreekt. Voor de accreditatie in 2025 zijn we van plan hier een monitor voor te gebruiken om inzichtelijk te krijgen wat we waar doen en of belangrijke thema’s aan bod komen. We hebben gemerkt bij de pogingen die we gedaan hebben, dat de balans tussen volledigheid en werkbaarheid een uitdaging is. We zijn benieuwd hoe dit bij andere opleidingen gaat, hoe het panel aankijkt tegen de bruikbaarheid van een BoKSA en wat en hoe gedetailleerd deze zou moeten worden uitgewerkt.
